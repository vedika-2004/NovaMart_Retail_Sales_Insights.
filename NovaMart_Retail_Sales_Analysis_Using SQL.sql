CREATE DATABASE retail_analysis;

USE retail_analysis;


CREATE TABLE sales_data (
    `Row ID` INT,
    `Order ID` VARCHAR(50),
    `Order Date` DATE,
    `Ship Date` DATE,
    `Ship Mode` VARCHAR(50),
    `Customer ID` VARCHAR(50),
    `Customer Name` VARCHAR(100),
    Segment VARCHAR(50),
    Country VARCHAR(50),
    City VARCHAR(50),
    State VARCHAR(50),
    `Postal Code` VARCHAR(20),
    Region VARCHAR(50),
    `Product ID` VARCHAR(50),
    Category VARCHAR(50),
    `Sub Category` VARCHAR(50),
    `Product Name` TEXT,
    Sales FLOAT,
    Quantity INT,
    Discount DECIMAL(5,2),
    Profit FLOAT,
    `Profit Margin` DECIMAL(5,2),
    `Order Year` INT,
    `Order Month` TINYINT
);


